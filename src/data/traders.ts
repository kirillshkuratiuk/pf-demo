function generateDailyPnl(): { date: string; pnl: number }[] {
  const days: { date: string; pnl: number }[] = [];
  const now = new Date(2026, 2, 30); // March 30, 2026
  for (let i = 364; i >= 0; i--) {
    const date = new Date(now);
    date.setDate(date.getDate() - i);
    // ~70% chance of having activity on a day
    const active = Math.random() > 0.3;
    const pnl = active
      ? (Math.random() - 0.4) * 10000 * (1 + Math.random() * 3)
      : 0;
    days.push({
      date: date.toISOString().split("T")[0],
      pnl: Math.round(pnl * 100) / 100,
    });
  }
  return days;
}

